#ifndef COST_HPP
#define COST_HPP

////////////////////////////////////////////////////////////
///	INCLUDES
////////////////////////////////////////////////////////////
#include "../util/Tensor.hpp"
#include "../util/Macros.hpp"
#ifdef CUDA_BACKEND
#include "../util/TensorCUDA.hpp"
#endif

////////////////////////////////////////////////////////////
///	NAMESPACE AI
////////////////////////////////////////////////////////////
namespace ai
{
	
	////////////////////////////////////////////////////////////
	/// \brief	A function that return a number representing how
	/// well the neural network performed to map inputs and outputs
	///
	////////////////////////////////////////////////////////////
	class Cost
	{
		public:

			//Cost function types
			enum CostType
			{
				SquaredError = 0,
				CrossEntropy = 1,
			};

			////////////////////////////////////////////////////////////
			/// \brief	Default constructor	
			///
			////////////////////////////////////////////////////////////
			Cost();

			////////////////////////////////////////////////////////////
			/// \brief	Construct from cost function type	
			///
			////////////////////////////////////////////////////////////
			Cost(const CostType type);

			#ifdef CUDA_BACKEND
			
			////////////////////////////////////////////////////////////
			/// \brief	Calculate errors
			///
			////////////////////////////////////////////////////////////
			float getErrorCUDA(TensorCUDA_float& prediction, TensorCUDA_float& target);

			////////////////////////////////////////////////////////////
			/// \brief	Calculate deltas
			///
			////////////////////////////////////////////////////////////
			void getDeltaCUDA(TensorCUDA_float& prediction, TensorCUDA_float& target, TensorCUDA_float& errors);

			#else
			
			////////////////////////////////////////////////////////////
			/// \brief	Calculate errors
			///
			////////////////////////////////////////////////////////////
			float getError(Tensor_float& prediction, Tensor_float& target);

			////////////////////////////////////////////////////////////
			/// \brief	Calculate deltas
			///
			////////////////////////////////////////////////////////////
			void getDelta(Tensor_float& prediction, Tensor_float& target, Tensor_float& errors);

			#endif

		private:
			CostType _type;
			#ifdef CUDA_BACKEND
			TensorCUDA_float _gpu_errors;
			Tensor_float _host_errors;
			#endif
	};

} //namespace ai

#endif /* end of include guard: COST_HPP */

